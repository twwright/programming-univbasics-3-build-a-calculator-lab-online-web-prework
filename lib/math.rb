def addition(num1, num2)
  added = num1 + num2
  returns added
end

def subtraction(num1, num2)
  subtracted = num1 - num2
  returns subtracted
end

def division(num1, num2)
  divided = num1 / num2
  returns divided
end

def multiplication(num1, num2)
  multiplied = num1 * num2
  returns multiplied
end

def modulo(num1, num2)
  modulod = num1 & num2
  returns modulod
end

def square_root(num)
  rooted = Math.sqrt(num)
  returns rooted
end
